import * as React from "react"
import { cn } from "@/lib/utils"

// Đây là component <Card> chính (cái khung bên ngoài)
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border bg-white text-gray-900 shadow-sm transition-all hover:shadow-md", // Thêm hiệu ứng shadow khi hover
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

